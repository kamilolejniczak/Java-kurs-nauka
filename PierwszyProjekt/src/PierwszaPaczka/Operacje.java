package PierwszaPaczka;						//J54

public class Operacje {

	
	public int dodaj(int a, int b){
		return a+b; 
	}
	
	public int dodaj(int a, int b, int c) {
		return a+ b+c;
	}
	
	public double dodaj(double a, double b){
		return a+b; 
	}
	
	public double dodaj(double a, double b, double c) {
		return a+ b+c;
	}
	
	

}
